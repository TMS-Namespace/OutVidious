namespace TMS.Libs.Frontend.Web.DockPanels.Enums;

/// <summary>
/// Represents the drawer direction for a dock panel.
/// </summary>
public enum DockPanelDrawerDirection
{
    /// <summary>
    /// No drawer direction specified.
    /// </summary>
    None = 0,

    /// <summary>
    /// Drawer opens from the left.
    /// </summary>
    Left = 1,

    /// <summary>
    /// Drawer opens from the right.
    /// </summary>
    Right = 2
}
