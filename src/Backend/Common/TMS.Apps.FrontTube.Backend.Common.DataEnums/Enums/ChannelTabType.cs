namespace TMS.Apps.FrontTube.Backend.Common.DataEnums.Enums;

/// <summary>
/// Represents the different tabs available on a YouTube channel.
/// </summary>
public enum ChannelTabType
{
    /// <summary>
    /// Regular videos tab.
    /// </summary>
    Videos,

    /// <summary>
    /// Shorts videos tab.
    /// </summary>
    Shorts,

    /// <summary>
    /// Live streams and premieres tab.
    /// </summary>
    Streams,

    /// <summary>
    /// Playlists tab.
    /// </summary>
    Playlists,

    /// <summary>
    /// Community posts tab.
    /// </summary>
    Community,

    /// <summary>
    /// Related channels tab.
    /// </summary>
    Channels,

    /// <summary>
    /// Latest videos tab (newest first).
    /// </summary>
    Latest,

    /// <summary>
    /// Podcasts tab.
    /// </summary>
    Podcasts,

    /// <summary>
    /// Music releases tab.
    /// </summary>
    Releases,

    Posts
}
